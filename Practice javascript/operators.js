// Arithmetic operators
console.log(4+5);
console.log(4-5);
console.log(4*5);
console.log(4/5);
console.log(5%4);

//Assignment Operators
let x=7
console.log(x+=5);

let y=7
console.log(y-=5);

//comparision operators
console.log(5>7);
console.log(5<7);
console.log(5>=5);
console.log(5<=4);
console.log(5=="5");
console.log(5==="5");

//logical operators
console.log(5>2 && 4===4);
console.log(5<2 || 4===4);
console.log(5<-2 && 7>-8);

//ternary operator
const age=21;
const status=age>=18?"Adult":"Minor";
